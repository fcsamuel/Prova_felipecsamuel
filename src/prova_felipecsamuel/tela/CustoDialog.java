    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        
        
        
        Custo c = new Custo();
        c.setCodigo(Integer.parseInt(tfCodigo.getText()));
        c.setDescricao(tfDescricao.getText());
        c.setTipo(Integer.parseInt(tfTipo.getText()));
        c.setValor(Double.parseDouble(tfValor.getText()));
        try {
            c.setDestino(dDao.getById(Integer.parseInt(tfDestino.getText())));
            cDao.save(c);
        } catch (SQLException sqle) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar.");
            sqle.printStackTrace();
        }
    }//GEN-LAST:event_btSalvarActionPerformed

